export function generateSectionPaths() {
  const sections = bankOfAmericaStadiumData.sections;
  const paths = [];
  
  for (const section of sections) {
    let color = "#4A90E2";
    
    switch (section.level_type) {
      case "field":
        color = "#2E7D32";
        break;
      case "lower":
        color = "#1976D2";
        break;
      case "club":
        color = "#7B1FA2";
        break;
      case "upper":
        color = "#424242";
        break;
    }
    
    if (section.pricing_tier === "premium") {
      color = "#FF8F00";
    } else if (section.pricing_tier === "field") {
      color = "#388E3C";
    }
    
    paths.push({
      section_id: section.section_number,
      section_name: `${section.level_type.charAt(0).toUpperCase() + section.level_type.slice(1)} Level ${section.section_number}`,
      level_type: section.level_type,
      pricing_tier: section.pricing_tier,
      default_color: color,
      hover_color: "#FF5722"
    });
  }
  
  return paths;
}
